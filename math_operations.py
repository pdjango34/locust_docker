# math_operations.py

def add(a, b):
    """
    Function to add two numbers.
    :param a: First number
    :param b: Second number
    :return: Sum of a and b
    """
    return a + b

def subtract(a, b):
    """
    Function to subtract two numbers.
    :param a: First number
    :param b: Second number
    :return: Difference of a and b
    """
    return a - b


def multiply(a, b):
    return a*b